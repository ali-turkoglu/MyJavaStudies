package day05_Concatenation;

/*  Create a class called StudentInfo, and declare variables for the followings:
        1. student name (String)
        2. age (int)
        3. gender (char)
        4. school name (String)
        5. phone number (String) (###)-###-####
        6. full time (boolean)

        */

public class StudentInfo {
    public static void main(String[] args) {

        String name = "Jimmy";
        int age = 45;
        char gender = 'M';
        String schoolName = "XX Collage";
        String phoneNumber1 = "(222)-233-3333";
        String phoneNumber2 = "(222)-222-2222";
        boolean fullTime = false;
        double GPA = 2.5;

        System.out.println("name = " + name);
        System.out.println("age = " + age);
        System.out.println("gender = " + gender);
        System.out.println("schoolName = " + schoolName);
        System.out.println("phoneNumber1 = " + phoneNumber1);
        System.out.println("phoneNumber2 = " + phoneNumber2);
        System.out.println("fullTime = " + fullTime);
        System.out.println("GPA = " + GPA);





    }

}
