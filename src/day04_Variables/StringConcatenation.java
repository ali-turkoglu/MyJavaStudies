package day04_Variables;

public class StringConcatenation {
    public static void main(String[] args) {

// concatenation = birbirine bağlamak

        String language = "Java";
        System.out.println("My favorite language is " + language);


        System.out.println();
        System.out.println("-----------------------------");
        System.out.println();


        String ad = "Mehmet";
        int yas = 55;
        System.out.println("Benim adım " + ad +" ve yaşımda " + yas + "'dir.");


    }


}
